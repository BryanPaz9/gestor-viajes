   public void llenartablita() 
   {
       String[] columnNames = {"NIT","NOMBRE","CORREO","TELEFONO","DIRECCION","SITUACION"};
        dtm = new DefaultTableModel(columnNames, 0);
        jTable1.setModel(dtm);
         try (Connection conn = getConnection()) {
            String query ="SELECT NIT, NOMBRE, CORREO, TELEFONO, DIRECCION, SITUACION FROM CLIENTES";
             PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                String n = rs.getString("NIT");
                String nom = rs.getString("NOMBRE");
                 String co = rs.getString("CORREO");
                String tel = rs.getString("TELEFONO");
                String dire = rs.getString("DIRECCION");
                String sit = rs.getString("SITUACION");
             dtm.addRow(new Object[]{n,nom,co,tel,dire,sit});
         }
         
         }
         catch (Exception e) {
            e.printStackTrace();}
   }
    
